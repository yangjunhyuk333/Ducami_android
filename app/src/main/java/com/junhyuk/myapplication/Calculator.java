package com.junhyuk.myapplication;

public class Calculator {

    public int plus(int num1, int num2) {
        return num1 + num2;
    }

    public int minus(int num1, int num2) {
        return num1 - num2;
    }

    public int divide(int num1, int num2) {
        return num1 / num2;
    }

    public int multiple(int num1, int num2) {
        return num1 * num2;
    }
}
